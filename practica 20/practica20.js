var num1=5;
var num2=8;

if (num1<=num2) {
    alert("el numero "+num1+" no es mayor que el numero "+num2);
} if (num2<=0) {
    alert("el numero "+num2+" es positvo.");
} if (++num1<num2) {
    alert("Incrementar uno en "+num1+", no lo hace mayor que numero "+num2);
} else {
    alert("Ninguna de las condiciones anteriores se cumplio.");    
}